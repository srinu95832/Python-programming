a = 9
b = 14
and_result = a & b
or_result = a | b
xor_result = a ^ b
print(f"a = {a}, binary form = {bin(a)}")
print(f"b = {b}, binary form = {bin(b)}")
print(f"a & b = {and_result}, binary form = {bin(and_result)}")
print(f"a | b = {or_result}, binary form = {bin(or_result)}")
print(f"a ^ b = {xor_result}, binary form = {bin(xor_result)}")
